    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(t1.getText().isEmpty()||t2.getText().isEmpty()||t3.getText().isEmpty()||t4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Datos Vacios","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            nom=t1.getText();
            nom=nom.toUpperCase();
            dir=t2.getText();
            dir.toUpperCase();
            ri=Integer.parseInt(t3.getText());
            if(ri<=0)
            {
                JOptionPane.showMessageDialog(null,"Solo puede ingresar numeros positivos","Registro inicial",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
             rf=Integer.parseInt(t4.getText());
            if(rf<=ri||rf<0||ri<0)
            {
                JOptionPane.showMessageDialog(null,"Los registros tienen que ser mayor a cero\nEl registro final tiene que ser menor al registro inicial","Error",JOptionPane.ERROR_MESSAGE);
                
            }
            else
            {
                kvh=rf-ri;
			//condicion1
			//140 kvh o menos 150 pesos
			if(kvh<=140)
			{
				tp=150;
			}
			//condicional 2
			//los siguientes 170 kvh 5.50 c/u
			if((kvh>140)&&kvh<=310)
			{
				tp=(float) (150+((kvh-140)*5.50));
			}
			if(kvh>310)
			{
				tp=(float) (150+(170*5.50)+((kvh-310)*3.50));
			}
                        ReciboLuz RL=new ReciboLuz();
                        RL.setVisible(true);
                        RL.l1.setText(nom.toString());
                        RL.l2.setText(dir.toString());
                        RL.l3.setText(Integer.toString(ri));
                        RL.l4.setText(Integer.toString(rf));
                        RL.l5.setText(Integer.toString(kvh));
                        RL.l6.setText(r.format(tp));
                        
            }   
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

